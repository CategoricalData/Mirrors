<h2>Key Features</h2>
	<ul>
		<li><b>Flexible I/O:</b> data can be imported into and exported out of CQL <a href="jdbc.php">by JDBC-SQL</a>, CSV, and more.</li>
		<li><b>Visualization:</b> CQL <a href="images/shot2.png">schemas</a>, <a href="images/shot3.png">databases</a>, etc. can be displayed graphically.</li>
		<li><b>Rich data integrity constraints:</b> CQL schemas contain entities, attributes, and foreign keys - as well as <a href="eqs.php">equations between them</a>.  One use of equations is for <a href="denorm.php">denormalization without the need to manually enforce the consistency of redundant data</a>.</li>   
		<li><b>Computational schemas:</b> <a href="unitconv.php">user-defined functions are part of CQL schemas</a> and can be specified  using java, javascript, or purely equationally.  CQL's theorem prover can reason about user-defined functions and how they relate to data integrity constraints.</li>  
		<li><b>More principled than SQL:</b> relational concepts such as <a href="joinless.php">foreign keys re-appear, in a more principled form, in CQL</a>.  And <a href="quotient.php">CQL provides primitives that SQL lacks</a>.</li>			
		<li><b>100% java</b>.  User-defined functions can be written in java or javascript, and a deep embedding of CQL into Haskell, in collaboration with <a href="https://statebox.org">Statebox</a>, is under development. </li>
		
		<li><b>Stateless</b>.  CQL is not a database management system: it neither stores nor updates data.  It is a <a href="http://categoricaldata.net/cql/fql_def.pdf">canonical functional programming language</a> and IDE whose scalability is similar to that of SQL and <a href="https://www.cs.ox.ac.uk/boris.motik/pubs/bkmmpst17becnhmarking-chase.pdf">chase engines</a>.  </li>
		
	</ul>
	
