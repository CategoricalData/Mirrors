<h2>Value Proposition</h2> 

	<ul>
	
	<li><b>Reduce risk of failure through artificial intelligence</b>.  CQL contains an embedded automated theorem prover that guarantees the correctness of CQL programs.  For example, <a href="fk.php">a CQL program cannot materialize an instance that violates a data integrity constraint</a>.  Such errors are detected at compile time, when they are easiest to fix. </li>
	
	<li><b>Preserve data quality</b>. High-quality data is expensive to obtain, so it is important to preserve that quality throughout the data life-cycle. CQL programs evolve and migrate data in a mathematically universal way, with zero degradation. As such, data integrated by CQL has many advantages, including perfect provenance: <a href="joinless.php">every row in the output of an CQL program contains a lineage that describes exactly how that row was obtained from input data</a>.</li>
	
	<li><b>Increased developer productivity through higher-level abstractions</b>.  CQL generalizes concepts from SQL using powerful principles from category theory.  For example, <a href="fk.php">CQL generalizes SQL's select-from-where queries from returning single tables to returning many tables related by foreign keys</a>.  Such higher-level abstractions enable developers to be more productive. </li>

	
	
	</ul>
