<h2>A principled way to transform data</h2> 

<p>
Open-source CQL and its integrated development environment (IDE)</a> performs data-related tasks &mdash; such as querying, combining, migrating, and evolving databases &mdash; using <a href="https://en.wikipedia.org/wiki/Applied_category_theory">category theory</a>, a branch of mathematics that has revolutionized several areas of computer science.  Open-source CQL is production-ready for single-node in-memory data processing workloads, such as <a href="http://arxiv.org/abs/1903.10579">integrating data for data science</a>.  It is being commercialized by <a href="https://conexus.com">Conexus AI</a>; for inquiries, contact <a href="https://wisnesky.net">Ryan Wisnesky</a>. 
